package main

import (
	"reflect"
	"testing"
)

func TestOne(t *testing.T) {
	got := gridSearch([]string{
		"7283455864",
		"6731158619",
		"8988242643",
		"3830589324",
		"2229505813",
		"5633845374",
		"6473530293",
		"7053106601",
		"0834282956",
		"4607924137",
	}, []string{
		"9505",
		"3845",
		"3530",
	})
	want := "YES"
	if !reflect.DeepEqual(got, want) {
		t.Errorf(`gridSearch([]string{
		"7283455864",
		"6731158619",
		"8988242643",
		"3830589324",
		"2229505813",
		"5633845374",
		"6473530293",
		"7053106601",
		"0834282956",
		"4607924137",
	}, []string{
		"9505",
		"3845",
		"3530",
	}) was incorrect, got: %v, want: %v`, got, want)
	}
}

